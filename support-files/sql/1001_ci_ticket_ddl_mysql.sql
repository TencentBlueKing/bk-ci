  `CERT_TEAM_NAME` varchar(128) NOT NULL COMMENT '证书 团队名称',
  `CERT_UUID` varchar(64) NOT NULL COMMENT '证书 uuid',
  `CERT_UPDATE_TIME` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
